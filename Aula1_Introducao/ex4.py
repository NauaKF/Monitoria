# Atribuir um valor à variável num
num = 5  # Você pode alterar este valor conforme necessário

# Calcular o quadrado de num e atribuir o resultado de volta a num
num = num ** 2

# Imprimir o valor de num
print(num)

#A variável num é inicialmente atribuída com o valor 5 (você pode mudar o valor de num para qualquer número).

#O quadrado de num é calculado usando a operação num ** 2, q
# ue eleva num à potência de 2. O resultado é então atribuído de volta à própria variável num.

#Finalmente, o valor de num é impresso, que agora será o quadrado do valor original.